                    <p align="justify">
                        <u><strong>#</strong></u>
                        <u><strong>Complications</strong></u>
                    </p>
                    <p align="justify">
                        Weeks 28 through 40 bring the arrival of the third trimester. This
                        exciting time is definitely the home stretch for expectant mothers,
                        but it’s also a time when complications can occur. Just as the
                        first two trimesters can bring their own challenges, so can the
                        third.
                    </p>
                    <ul>
                        <li>
                            <p align="justify">
                                <a name="gestational-diabetes"></a>
                                Prenatal care is especially important in the third
                                trimester because the types of complications that can arise
                                at this time are more easily managed if detected
                                early.You’ll likely start visiting your obstetrician every
                                other week from 28 to 36 weeks and then once per week until
                                your little one arrives.
                            </p>
                        </li>
                    </ul>
                    <ol>
                        <li>
                            <p align="justify">
                                <u><strong>G</strong></u>
                                <u><strong>estational diabetes:</strong></u>
                            </p>
                        </li>
                    </ol>
                    <ul>
                        <li>
                            <p align="justify">
                                As many as 9.2 percent of pregnant women have gestational
                                diabetes. Gestational diabetes occurs because the hormonal
                                changes of pregnancy make it more difficult for your body
                                to effectively use insulin. When insulin can’t do its job
                                of lowering blood sugar to normal levels, the result is
                                abnormally high glucose (blood sugar) levels.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Most women have no symptoms. While this condition isn’t
                                usually dangerous for the mother, it poses several problems
                                for the fetus. Specifically,
                                <a href="https://www.healthline.com/health/macrosomia">
                                    macrosomia
                                </a>
                                (excessive growth) of the fetus can increase the likelihood
                                of cesarean delivery and the risk of birth injuries. When
                                glucose levels are well-controlled, macrosomia is less
                                likely.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                At the beginning of the third trimester (between weeks 24
                                and 28), all women should get tested for gestational
                                diabetes.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                During the glucose tolerance test (also known as the
                                screening glucose challenge test), you’ll consume a drink
                                that contains a certain amount of glucose (sugar). At a
                                specified time later, your doctor will test your blood
                                sugar levels.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                For the oral glucose tolerance test, you fast for at least
                                eight hours and then have 100 milligrams of glucose, after
                                which your blood sugar levels are checked. Those levels
                                will be measured at one, two, and three hours after you
                                drink the glucose.
                            </p>
                        </li>
                    </ul>
                    <p align="justify">
                        The typical expected values are:
                    </p>
                    <ul>
                        <li>
                            <p align="justify">
                                after fasting, is lower than 95 milligrams per deciliter
                                (mg/dL)
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                after one hour, is lower than 180 mg/dL
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                after two hours, is lower than 155 mg/dL
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                after three hours, is lower than 140 mg/dL
                            </p>
                        </li>
                    </ul>
                    <p align="justify">
                        If two of the three results are too high, a woman likely has
                        gestational diabetes.
                    </p>
                    <p align="justify">
                        <u><strong>Treatment</strong></u>
                    </p>
                    <ul>
                        <li>
                            <p align="justify">
                                Gestational diabetes can be treated with diet, lifestyle
                                changes, and medicines, in some instances. Your doctor will
                                recommend dietary changes, such as decreasing your
                                carbohydrate intake and increasing fruits and veggies.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Adding low-impact exercise can also help. In some
                                instances, your doctor may prescribe insulin.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                The good news is that gestational diabetes usually goes
                                away during the postpartum period. Blood sugars will be
                                monitored after delivery to be sure.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                However, a woman who has had gestational diabetes has a
                                higher risk of having diabetes later in life than a woman
                                who hasn’t had gestational diabetes.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                <a name="preeclampsia"></a>
                                The condition could also impact a woman’s chances of
                                becoming pregnant again. A doctor will likely recommend
                                checking a woman’s blood sugar levels to make sure they’re
                                under control before she tries to have another baby.
                            </p>
                        </li>
                    </ul>
                    <ol start="2">
                        <li>
                            <p align="justify">
                                <u><strong>Preeclampsia:</strong></u>
                            </p>
                        </li>
                    </ol>
                    <ul>
                        <li>
                            <p align="justify">
                                <a href="https://www.healthline.com/health/preeclampsia">
                                    Preeclampsia
                                </a>
                                is a serious condition that makes regular prenatal visits
                                even more important. The condition typically occurs after
                                20 weeks of pregnancy and can cause serious complications
                                for mom and baby.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Between
                                <a href="https://www.preeclampsia.org/health-information/about-preeclampsia" target="_blank">
                                    <u>5 </u>
                                    and<u> 8 </u>percent
                                </a>
                                of women experience the condition. Teenagers, women 35 and
                                older, and women pregnant with their first baby are at a
                                higher risk. African American women are at higher risk.
                            </p>
                        </li>
                    </ul>
                    <p align="justify">
                        <u><strong>Symptoms</strong></u>
                    </p>
                    <ul>
                        <li>
                            <p align="justify">
                                Symptoms of the condition include high blood pressure,
                                protein in the urine, sudden weight gain, and swelling of
                                the hands and feet. Any of these symptoms warrant further
                                assessment.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Prenatal visits are essential because screening done during
                                these visits can detect symptoms like high blood pressure
                                and increased protein in the urine. If left untreated,
                                preeclampsia can lead to
                                <u>
                                    <a href="https://www.healthline.com/health/eclampsia">
                                        eclampsia
                                    </a>
                                </u>
                                (seizures),
                                <u>
                                    <a href="https://www.healthline.com/health/kidney-failure">
                                        kidney failure
                                    </a>
                                </u>
                                , and, sometimes even death in the mother and fetus.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                The first sign that your doctor usually sees is high blood
                                pressure during a routine prenatal visit. Also, protein
                                might be detected in your urine during a
                                <u>
                                    <a href="https://www.healthline.com/health/urinalysis">
                                        urinalysis
                                    </a>
                                </u>
                                . Some women may gain more weight than expected. Others
                                experience headaches, vision changes, and upper abdominal
                                pain.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Women should never ignore symptoms of preeclampsia.
                            </p>
                        </li>
                    </ul>
                    <p align="justify">
                        Seek emergency medical treatment if you have rapid swelling in the
                        feet and legs, hands, or face. Other emergency symptoms include:
                    </p>
                    <ul>
                        <li>
                            <p align="justify">
                                headache that doesn’t go away with medication
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                loss of vision
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                “floaters” in your vision
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                severe pain on your right side or in your stomach area
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                easy bruising
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                decreased amounts of urine
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                shortness of breath
                            </p>
                        </li>
                    </ul>
                    <p align="justify">
                        These signs may suggest severe preeclampsia. Blood tests, such as
                        liver and kidney function tests and blood-clotting tests, may
                        confirm the diagnosis and can detect severe disease.
                    </p>
                    <p align="justify">
                        <u><strong>Treatment</strong></u>
                    </p>
                    <ul>
                        <li>
                            <p align="justify">
                                How your doctor treats preeclampsia depends on its severity
                                and how far along in the pregnancy you are. Delivering your
                                baby may be necessary to protect you and your little one.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Your doctor will discuss several considerations with you
                                depending on your weeks of gestation. If you are close your
                                due date it may be safest to deliver the baby.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                You may have to stay at the hospital for observation and to
                                manage your blood pressure until the baby is old enough for
                                delivery. If your baby is younger than 34 weeks, you will
                                probably be given medication to speed up the baby’s lung
                                development.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Preeclampsia can continue past delivery, though for most
                                women symptoms start to diminish after giving birth.
                                However, sometimes blood pressure medication is prescribed
                                for a short time after delivery.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Diuretics may be prescribed to treat pulmonary edema (fluid
                                in the lungs). Magnesium sulfate given before, during, and
                                after the delivery can help to reduce seizure risks. A
                                woman who has had preeclampsia symptoms before delivery
                                will continue to be monitored after the baby is born.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                If you’ve had preeclampsia, you’re
                                <u>
                                    <a href="https://www.mayoclinic.org/diseases-conditions/preeclampsia/symptoms-causes/syc-20355745" target="_blank">
                                        at greater risk
                                    </a>
                                </u>
                                for having the condition with future pregnancies. Always
                                talk with your doctor about how you can lower your risk.
                            </p>
                        </li>
                    </ul>
                    <h3 align="justify">
                        <u>Cause and prevention</u>
                    </h3>
                    <ul>
                        <li>
                            <p align="justify">
                                Despite years of scientific study, the true cause of
                                preeclampsia isn’t known, nor is there any effective
                                prevention. The treatment, however, has been known for many
                                decades and that is delivery of the baby.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                <a name="preterm-labor"></a>
                                The problems associated with preeclampsia can continue even
                                after delivery, but this is unusual. Timely diagnosis and
                                delivery is the best way to avoid serious problems for
                                mother and baby.
                            </p>
                        </li>
                    </ul>
                    <ol start="3">
                        <li>
                            <p align="justify">
                                <u><strong>preterm labor:</strong></u>
                            </p>
                        </li>
                    </ol>
                    <p align="justify">
                        Preterm labor occurs when you start having contractions that cause
                        cervical changes before you’re 37 weeks pregnant.
                    </p>
                    <p align="justify">
                        Some women are at greater risk for preterm labor, including those
                        who:
                    </p>
                    <ul>
                        <li>
                            <p align="justify">
                                are pregnant with multiples (twins or more)
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                have an infection of the amniotic sac (
                                <u>
                                    <a href="https://www.healthline.com/health/pregnancy/complications-delivery-amnionitis">
                                        amnionitis
                                    </a>
                                </u>
                                )
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                have excess amniotic fluid (polyhydramnios)
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                have had a previous preterm birth
                            </p>
                        </li>
                    </ul>
                    <h3 align="justify">
                        <u>Symptoms</u>
                    </h3>
                    <p align="justify">
                        Signs and symptoms of preterm labor can be subtle. An expectant mom
                        may pass them off as part of pregnancy. Symptoms include:
                    </p>
                    <ul>
                        <li>
                            <p align="justify">
                                diarrhea
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                frequent urination
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                lower back pain
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                tightness in the lower abdomen
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                vaginal discharge
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                vaginal pressure
                            </p>
                        </li>
                    </ul>
                    <p align="justify">
                        Of course, some women may experience more severe labor symptoms.
                        These include regular, painful contractions, leaking of fluid from
                        the vagina, or vaginal bleeding.
                    </p>
                    <h3 align="justify">
                        <u>Treatment</u>
                    </h3>
                    <ul>
                        <li>
                            <p align="justify">
                                Infants born prematurely are at risk for health problems
                                because their bodies haven’t had time to fully develop. One
                                of the greatest concerns is lung development because the
                                lungs develop well into the third trimester. The younger a
                                baby is when born, the greater the possible complications.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Doctors don’t know the exact cause of premature labor.
                                However, it’s important for you to receive care as soon as
                                possible. Sometimes medicines like magnesium sulfate can
                                help stop preterm labor and delay delivery.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Each day your pregnancy is prolonged increases your chances
                                for a healthy baby.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Doctors often give a steroid medication to moms whose
                                preterm labor starts before 34 weeks. This helps your
                                baby’s lungs mature and reduces the severity of lung
                                disease if your labor can’t be stopped.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Steroid medication has its peak effect within two days, so
                                it’s best to prevent delivery for at least two days, if
                                possible.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                All women with preterm labor who haven’t been tested for
                                the presence of group B streptococcus should receive
                                antibiotics (penicillin G, ampicillin, or an alternative
                                for those who are allergic to penicillin) until delivery.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                If preterm labor begins after 36 weeks, the baby is usually
                                delivered since the risk of lung disease from prematurity
                                is very low.
                            </p>
                        </li>
                    </ul>
                    <ol start="4">
                        <li>
                            <h2 align="justify">
                                <a name="prom"></a>
                                <u>
                                    <strong>Premature rupture of membranes (PROM)</strong>
                                </u>
                            </h2>
                        </li>
                    </ol>
                    <ul>
                        <li>
                            <p align="justify">
                                Rupture of membranes is a normal part of giving birth. It’s
                                the medical term for saying your “water has broken.” It
                                means that the amniotic sac that surrounds your baby has
                                broken, allowing the amniotic fluid to flow out.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                While it’s normal for the sac to break during labor, if it
                                happens too early, it can cause serious complications. This
                                is called preterm / premature rupture of membranes (PROM).
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Although the cause of PROM isn’t always clear, sometimes an
                                infection of the amniotic membranes is the cause and other
                                factors, such as genetics, come into play.
                            </p>
                        </li>
                    </ul>
                    <h3 align="justify">
                        <u>Treatment</u>
                    </h3>
                    <ul>
                        <li>
                            <p align="justify">
                                Treatment for PROM varies. Women are often hospitalized and
                                given antibiotics, steroids, and drugs to stop labor
                                (tocolytics).
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                When PROM occurs at 34 weeks or more, some doctors might
                                recommend delivering the baby. At that time, the risks of
                                prematurity are less than the infection risks. If there are
                                signs of infection, labor must be induced to avoid serious
                                complications.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Occasionally, a woman with PROM experiences resealing of
                                the membranes. In these rare cases, a woman can continue
                                her pregnancy to near term, although still under close
                                observation.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                The risks associated with prematurity
                                <u>
                                    <a href="https://medlineplus.gov/ency/patientinstructions/000512.htm" target="_blank">
                                        decrease significantly
                                    </a>
                                </u>
                                as the fetus nears term. If PROM occurs in the 32- to
                                34-week range and the remaining amniotic fluid shows that
                                the fetus’ lungs have matured enough, the doctor may
                                discuss delivering the baby in some cases.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                With improved intensive care nursery services, many preterm
                                infants born in the third trimester (after 28 weeks) do
                                very well.
                            </p>
                        </li>
                        <li>
                            <h2 align="justify">
                                <a name="placenta-problems"></a>
                                Problems with the placenta (previa and abruption)
                            </h2>
                        </li>
                        <li>
                            <p align="justify">
                                Bleeding in the third trimester may have several causes.
                                The more serious causes are placenta previa and placental
                                abruption.
                            </p>
                        </li>
                    </ul>
                    <ol start="5">
                        <li>
                            <h3 align="justify">
                                <u>Placenta previa</u>
                            </h3>
                        </li>
                    </ol>
                    <ul>
                        <li>
                            <p align="justify">
                                The placenta is the organ that nourishes your baby while
                                you’re pregnant. Usually, the placenta is delivered after
                                your baby. However, women with
                                <u>
                                    <a href="https://www.healthline.com/health/placenta-previa">
                                        placenta previa
                                    </a>
                                </u>
                                have a placenta that comes first and blocks the opening to
                                the cervix.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Doctors don’t know the exact cause of this condition. Women
                                who’ve had a previous cesarean delivery or uterine surgery
                                are at greater risk. Women who smoke or have a
                                larger-than-normal placenta are also at greater risk.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Placenta previa increases risk of bleeding before and
                                during delivery. This can be life-threatening.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                A common symptom of placenta previa is bright red, sudden,
                                profuse, and painless vaginal bleeding, which usually
                                occurs after the 28th week of pregnancy. Doctors usually
                                use an ultrasound to identify placenta previa.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Treatment depends on whether the fetus is preterm and the
                                amount of bleeding. If labor is unstoppable, the baby is in
                                distress, or there is life-threatening hemorrhage,
                                immediate cesarean delivery is indicated no matter the
                                fetus’ age.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                If the bleeding stops or isn’t too heavy, delivery can
                                often be avoided. This allows more time for the fetus to
                                grow if the fetus is near-term. A doctor usually recommends
                                cesarean delivery.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Thanks to modern obstetric care, ultrasound diagnosis, and
                                the availability of blood transfusion, if needed, women
                                with placenta previa and their infants usually do well.
                            </p>
                        </li>
                    </ul>
                    <ol start="6">
                        <li>
                            <h3 align="justify">
                                <u>Placental abruption</u>
                            </h3>
                        </li>
                    </ol>
                    <p align="justify">
                        <a href="https://www.healthline.com/health/pregnancy/complications-placental-abruption">
                            Placental<u> </u>abruption
                        </a>
                        is a rare condition in which the placenta separates from the uterus
                        prior to labor. It occurs in up to
                        <a href="https://www.ncbi.nlm.nih.gov/pubmed?term=26018653" target="_blank">
                            <u>1 </u>
                            percent<u> </u>Trusted Source
                        </a>
                        of pregnancies. Placental abruption can result in fetal death and
                        can cause serious bleeding and shock in the mother.
                    </p>
                    <p align="justify">
                        Risk factors for placental abruption include:
                    </p>
                    <ul>
                        <li>
                            <p align="justify">
                                advanced maternal age
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                cocaine use
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                <u>
                                    <a href="https://www.healthline.com/health/diabetes">
                                        diabetes
                                    </a>
                                </u>
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                heavy alcohol use
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                high blood pressure
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                pregnancy with multiples
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                preterm premature rupture of the membranes
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                prior pregnancies
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                short umbilical cord
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                smoking
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                trauma to the stomach
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                uterine distention due to excess amniotic fluid
                            </p>
                        </li>
                    </ul>
                    <ul>
                        <li>
                            <p align="justify">
                                Placental abruption doesn’t always cause symptoms. But some
                                women experience heavy vaginal bleeding, severe stomach
                                pain, and strong contractions. Some women have no bleeding.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                A doctor can evaluate a woman’s symptoms and the baby’s
                                heartbeat to identify potential fetal distress. In many
                                instances, rapid cesarean delivery is necessary. If a woman
                                loses excess blood, she may also need a blood transfusion.
                            </p>
                        </li>
                    </ul>
                    <ol start="7">
                        <li>
                            <h2 align="justify">
                                <a name="iugr"></a>
                                <u>
                                    <strong>Intrauterine growth restriction (IUGR)</strong>
                                </u>
                            </h2>
                        </li>
                    </ol>
                    <ul>
                        <li>
                            <p align="justify">
                                Occasionally a baby won’t grow as much as they’re expected
                                to at a certain stage in a woman’s pregnancy. This is known
                                as
                                <u>
                                    <a href="https://www.healthline.com/symptom/growth-retardation">
                                        intrauterine growth restriction (IUGR)
                                    </a>
                                </u>
                                . Not all small babies have IUGR — sometimes their size can
                                be attributed to the smaller size of their parents.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                IUGR can result in symmetrical or asymmetrical growth.
                                Babies with asymmetrical growth often have a normal-sized
                                head with a smaller-sized body.
                            </p>
                        </li>
                    </ul>
                    <p align="justify">
                        Maternal factors that can lead to IUGR include:
                    </p>
                    <ul>
                        <li>
                            <p align="justify">
                                <u>
                                    <a href="https://www.healthline.com/symptom/anemia">
                                        anemia
                                    </a>
                                </u>
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                <u>
                                    <a href="https://www.healthline.com/health/chronic-kidney-disease">
                                        chronic renal disease
                                    </a>
                                </u>
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                <u>
                                    <a href="https://www.healthline.com/health/placenta-previa">
                                        placenta previa
                                    </a>
                                </u>
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                placental infarction
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                severe diabetes
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                severe malnutrition
                            </p>
                        </li>
                    </ul>
                    <ul>
                        <li>
                            <p align="justify">
                                Fetuses with IUGR may be less able to tolerate the stress
                                of labor than infants of normal size. IUGR babies also tend
                                to have less body fat and more trouble maintaining their
                                body temperature and glucose levels (blood sugar) after
                                birth.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                If growth problems are suspected, a doctor can use an
                                ultrasound to measure the fetus and calculate an estimated
                                fetal weight. The estimate can be compared with the range
                                of normal weights for fetuses of similar age.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                To determine whether the fetus is small for gestational age
                                or growth restricted, a series of ultrasounds is done over
                                to time to document weight gain or lack thereof.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                A specialized ultrasound monitoring umbilical blood flow
                                can also determine IUGR. Amniocentesis may be used to check
                                for chromosomal problems or infection. Monitoring the fetal
                                heart pattern and measurement of the amniotic fluid are
                                common.
                            </p>
                        </li>
                    </ul>
                    <p align="justify">
                        If a baby stops growing in the womb, a doctor may recommend
                        induction or cesarean delivery. Luckily, most growth-restricted
                        babies develop normally after birth. They tend to catch up in
                        growth by two years old.
                    </p>
                    <ol start="8">
                        <li>
                            <h2 align="justify">
                                <a name="post--term"></a>
                                <u><strong>Post-term pregnancy</strong></u>
                            </h2>
                        </li>
                    </ol>
                    <ul>
                        <li>
                            <p align="justify">
                                About
                                <u>
                                    <a href="https://www.chw.org/medical-care/fetal-concerns-center/conditions/pregnancy-complications/post-term-pregnancy" target="_blank">
                                        7 percent
                                    </a>
                                </u>
                                of women deliver at 42 weeks or later. Any pregnancy
                                lasting longer than 42 weeks is considered post-term or
                                post-dates. The cause of post-term pregnancy is unclear,
                                although hormonal and hereditary factors are suspected.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Sometimes, a woman’s due date isn’t calculated correctly.
                                Some women have irregular or long menstrual cycles that
                                make ovulation harder to predict. Early in pregnancy, an
                                ultrasound can help to confirm or adjust the due date.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Post-term pregnancy isn’t generally dangerous to the
                                mother’s health. The concern is for the fetus. The placenta
                                is an organ that is designed to work for about 40 weeks. It
                                provides oxygen and nutrition for the growing fetus.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                After 41 weeks of pregnancy, the placenta is less likely to
                                work well, and this may result in decreased amniotic fluid
                                around the fetus (oligohydramnios).
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                This condition can cause compression of the umbilical cord
                                and decrease oxygen supply to the fetus. This may be
                                reflected on the fetal heart monitor in a pattern called
                                late decelerations. There is a risk of sudden fetal death
                                when the pregnancy is post-term.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Once a woman reaches 41 weeks of pregnancy, she usually has
                                fetal heart rate monitoring and a measurement of the
                                amniotic fluid. If the testing shows low fluid levels or
                                abnormal fetal heart rate patterns, labor is induced.
                                Otherwise, spontaneous labor is awaited until no more than
                                42 to 43 weeks, after which it’s induced.
                            </p>
                        </li>
                    </ul>
                    <ul>
                        <li>
                            <h3 align="justify">
                                <u>Meconium aspiration syndrome</u>
                            </h3>
                        </li>
                        <li>
                            <p align="justify">
                                The other risk is meconium. Meconium is a fetus’ bowel
                                movement. It’s more common when the pregnancy is post-term.
                                Most fetuses that have a bowel movement inside the uterus
                                have no problems.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                However, a stressed fetus can inhale the meconium, causing
                                a very serious type of pneumonia and, rarely, death. For
                                these reasons, doctors work to clear a baby’s airway as
                                much as possible if a baby’s amniotic fluid is
                                meconium-stained.
                            </p>
                        </li>
                    </ul>
                    <ol start="9">
                        <li>
                            <h2 align="justify">
                                <a name="malpresentation"></a>
                                <u>
                                    <strong>
                                        Malpresentation (breech, transverse lie)
                                    </strong>
                                </u>
                            </h2>
                        </li>
                    </ol>
                    <ul>
                        <li>
                            <p align="justify">
                                As a woman approaches her ninth month of pregnancy, the
                                fetus generally settles into a head-down position inside
                                the uterus. This is known as vertex or cephalic
                                presentation.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                The fetus will be bottom or feet first (known as breech
                                presentation) in about
                                <u>
                                    <a href="https://www.acog.org/Patients/FAQs/If-Your-Baby-Is-Breech" target="_blank">
                                        3 to 4 percent
                                    </a>
                                </u>
                                of full-term pregnancies.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Occasionally, the fetus will be lying sideways (transverse
                                presentation).
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                The safest way for a baby to be born is head first or in
                                the vertex presentation. If the fetus is breech or
                                transverse, the best way to avoid problems with delivery
                                and prevent a cesarean is to try to turn (or vert) the
                                fetus to vertex presentation (head down). This is known as
                                external cephalic version. It’s usually attempted at 37 to
                                38 weeks, if the malpresentation is known.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                External cephalic version is somewhat like a firm massage
                                of the abdomen and can be uncomfortable. It’s usually a
                                safe procedure, but some rare complications include
                                placental abruption and fetal distress, necessitating
                                emergency cesarean delivery.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                If the fetus is successfully turned, spontaneous labor can
                                be awaited or labor can be induced. If it’s unsuccessful,
                                some doctors wait a week and try again. If unsuccessful
                                after reattempts, you and your doctor will decide the best
                                type of delivery, vaginal or cesarean.
                            </p>
                        </li>
                        <li>
                            <p align="justify">
                                Measurement of the bones of the mother’s birth canal and
                                ultrasound to estimate fetal weight are often obtained in
                                preparation for breech vaginal deliveries. Transverse
                                fetuses are delivered by cesarean.
                            </p>
                        </li>
