                                </a>
                            </u>
                        </p>
                    </li>
                    <li>
                        <p align="justify">
                            proteinuria (a large amount of protein in the urine)
                        </p>
                    </li>
                </ul>
                <p align="justify">
                    Preeclampsia affects every system in the body, including the placenta.
                </p>
                <p align="justify">
                    The placenta is responsible for providing nutrients to the baby. Though
                    preeclampsia typically occurs during the third trimester for first-time
                    pregnancies, some people develop preeclampsia during the second trimester.
                </p>
                <p align="justify">
                    Before making a diagnosis, your doctor will evaluate you for other
                    conditions that may be confused with preeclampsia, such as <u><a href="https://www.healthline.com/health/lupus">lupus</a></u> (which
                    causes inflammation throughout the body) and <u><a href="https://www.healthline.com/health/epilepsy">epilepsy</a></u> (a
                    seizure disorder).
                </p>
                <p align="justify">
                    Your doctor will also evaluate you for conditions that can increase the
                    likelihood of developing early preeclampsia, such as blood clotting
                    disorders and molar pregnancy. That’s a noncancerous tumor that forms in
                    the uterus.
                </p>
                <p align="justify">
                    <u><strong>Symptoms</strong></u>
                </p>
                <p align="justify">
                    Symptoms of preeclampsia include rapid swelling of your legs, hands, or
                    face. Call your doctor right away if you experience this type of swelling
                    or any of the following symptoms:
                </p>
                <ul>
                    <li>
                        <p align="justify">
                            headache that doesn’t go away after taking acetaminophen (Tylenol)
                        </p>
                    </li>
                    <li>
                        <p align="justify">
                            loss of vision
                        </p>
                    </li>
                    <li>
                        <p align="justify">
                            “floaters” in your eye (specks or spots in your vision)
                        </p>
                    </li>
                    <li>
                        <p align="justify">
                            severe pain on your right side or in your stomach area
                        </p>
                    </li>
                    <li>
                        <p align="justify">
