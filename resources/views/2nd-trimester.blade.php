                <h2 align="justify">
                    <a name="cervical-incompetence"></a>
                    <u><strong>Cervical incompetence (cervical insufficiency)</strong></u>
                </h2>
                <p align="justify">
                    The cervix is a tissue that connects the vagina and the uterus. Sometimes,
                    the cervix is unable to withstand the pressure of the growing uterus during
                    pregnancy. The increased pressure can weaken the cervix and cause it to
                    open before the ninth month.
                </p>
                <p align="justify">
                    This condition is known as cervical incompetence, or cervical
                    insufficiency. While it’s an uncommon condition, it can cause serious
                    complications.
                </p>
                <p align="justify">
                    The opening and thinning of the cervix eventually leads to the rupture of
                    membranes and delivery of a very premature fetus. This usually occurs
                    around the 20th week of pregnancy. Since the fetus is too premature to
                    survive outside the uterus at that point, the pregnancy often can’t be
                    saved.
                </p>
                <p align="justify">
                    Women are at a higher risk for cervical incompetence if they’ve had:
                </p>
                <ul>
                    <li>
                        <p align="justify">
                            a previous cervical trauma, such as a tear during delivery
                        </p>
                    </li>
                    <li>
                        <p align="justify">
                            a cervical cone biopsy
                        </p>
                    </li>
                    <li>
                        <p align="justify">
                            other operation on the cervix
                        </p>
                    </li>
                </ul>
                <h3 align="justify">
                    Symptoms
                </h3>
                <p align="justify">
                    Unlike preterm labor, cervical incompetence typically doesn’t cause pain or
                    contractions. There may be vaginal bleeding or discharge.
                </p>
                <h3 align="justify">
                    Treatment
                </h3>
                <p align="justify">
                    Treatment for cervical incompetence is limited. An emergency cerclage
                    (stitch around the cervix) is a possibility if the membranes haven’t
                    ruptured yet. The risk of rupturing the membranes is higher if the cervix
                    is very dilated (wide). Extended bed rest is necessary after the placement
                    of a cerclage.
                </p>
                <p align="justify">
                    In other cases, when the membranes have already ruptured and the fetus is
                    old enough to survive, your doctor will likely induce labor.
                </p>
                <h3 align="justify">
                    Prevention
                </h3>
                <p align="justify">
                    <a name="preeclampsia"></a>
