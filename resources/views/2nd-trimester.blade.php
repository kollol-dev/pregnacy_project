                        <p align="justify">
                            <u><strong>Skin changes.</strong></u>
                            Hormonal changes during pregnancy stimulate an increase in
                            pigment-bearing cells (melanin) in your skin. As a result, you
                            might notice brown patches on your face (melasma). You might also
                            see a dark line down your abdomen (linea nigra). These skin changes
                            are common and usually fade after delivery. Sun exposure, however,
                            can aggravate the issue. When you're outdoors, use sunscreen. You
                            might also notice reddish-brown, black, silver or purple lines
