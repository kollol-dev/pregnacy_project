                            <u><strong>Nasal problems.</strong></u>
                            During pregnancy, your hormone levels increase and your body makes
                            more blood. This can cause your mucous membranes to swell and bleed
                            easily, resulting in stuffiness and nosebleeds. Saline drops or a
                            saline rinse can help relieve congestion. Also, drink plenty of
                            fluids, use a humidifier, and dab petroleum jelly around the edges
                            of your nostrils to help moisten skin.
                        </p>
                    </li>
